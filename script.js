const openOrderPanelBtn = document.getElementById('openOrderPanelBtn');

let cart = [];

// Populate category dropdown
categories.forEach(cat => {
  const option = document.createElement('option');
  option.value = cat;
  option.textContent = cat;
  categorySelect.appendChild(option);
});

// Helper functions
function escapeHtml(str) {
  return str.replace(/[&<>]/g, function(m) {
    if (m === '&') return '&amp;';
    if (m === '<') return '&lt;';
    if (m === '>') return '&gt;';
    return m;
  });
}

function saveCart() {
  localStorage.setItem('royalTreatCart', JSON.stringify(cart));
}

function loadCart() {
  const saved = localStorage.getItem('royalTreatCart');
  if (saved) {
    try {
      cart = JSON.parse(saved);
      updateCartUI();
    } catch(e) {
      console.error('Error loading cart:', e);
      cart = [];
    }
  }
}

function updateCartUI() {
  const totalItems = cart.reduce((sum, item) => sum + item.quantity, 0);
  cartCountSpan.innerText = totalItems;

  if (cart.length === 0) {
    orderListContainer.innerHTML = '<div class="empty-msg">Cart is empty<br>Add delicious items!</div>';
    saveCart();
    return;
  }

  let html = '';
  let totalPrice = 0;
  cart.forEach((item) => {
    const itemTotal = item.price * item.quantity;
    totalPrice += itemTotal;
    html += `
      <div class="cart-item">
        <div class="cart-item-details">${escapeHtml(item.name)}</div>
        <div class="cart-item-price">₹${item.price}</div>
        <div class="quantity-controls">
          <button class="qty-btn minus-btn" data-name="${escapeHtml(item.name)}">−</button>
          <span class="qty-num">${item.quantity}</span>
          <button class="qty-btn plus-btn" data-name="${escapeHtml(item.name)}">+</button>
        </div>
      </div>
    `;
  });
  html += `<div class="total-row"><span>💰 Total Amount:</span><span>₹${totalPrice}</span></div>`;
  orderListContainer.innerHTML = html;

  // Attach event listeners for quantity buttons
  document.querySelectorAll('.plus-btn').forEach(btn => {
    btn.removeEventListener('click', plusHandler);
    btn.addEventListener('click', plusHandler);
  });
  document.querySelectorAll('.minus-btn').forEach(btn => {
    btn.removeEventListener('click', minusHandler);
    btn.addEventListener('click', minusHandler);
  });
  saveCart();
}

function plusHandler(e) {
  const name = e.currentTarget.getAttribute('data-name');
  updateQuantity(name, 1);
}

function minusHandler(e) {
  const name = e.currentTarget.getAttribute('data-name');
  updateQuantity(name, -1);
}

function updateQuantity(itemName, delta) {
  const existing = cart.find(i => i.name === itemName);
  if (existing) {
    const newQty = existing.quantity + delta;
    if (newQty <= 0) {
      cart = cart.filter(i => i.name !== itemName);
    } else {
      existing.quantity = newQty;
    }
    updateCartUI();
  }
}

function addToCart(itemName, itemPrice) {
  const existing = cart.find(i => i.name === itemName);
  if (existing) {
    existing.quantity += 1;
  } else {
    cart.push({ name: itemName, price: itemPrice, quantity: 1 });
  }
  updateCartUI();
  const badge = openOrderPanelBtn;
  badge.style.transform = 'scale(1.1)';
  setTimeout(() => badge.style.transform = '', 200);
}

function renderMenu() {
  const selectedCat = categorySelect.value;
  const searchTerm = searchInput.value.toLowerCase().trim();

  selectedCategorySpan.innerText = selectedCat === 'all' ? 'All Items' : selectedCat;

  let filteredItems = menuData.filter(item => {
    const matchesSearch = searchTerm === '' || item.name.toLowerCase().includes(searchTerm);
    const matchesCategory = (selectedCat === 'all') || (item.category === selectedCat);
    return matchesSearch && matchesCategory;
  });

  if (filteredItems.length === 0) {
    menuContainer.innerHTML = `<div style="text-align:center; padding:40px 20px; background:white; border-radius:20px;">
      <span style="font-size:2.5rem;">🍽️</span>
      <p style="margin-top:12px; color:#4a1d08; font-size:0.85rem;">No items match your search. Try something else!</p>
    </div>`;
    return;
  }

  // Group by category
  const grouped = {};
  filteredItems.forEach(item => {
    if (!grouped[item.category]) grouped[item.category] = [];
    grouped[item.category].push(item);
  });

  let finalHTML = '';
  for (const [catName, catItems] of Object.entries(grouped)) {
    const catImage = categoryImages[catName] || "https://images.unsplash.com/photo-1504674900247-0877df9cc836?w=400&h=250&fit=crop&auto=format";
    finalHTML += `<div class="menu-section">
      <div class="category-group">
        <h3>${escapeHtml(catName)}</h3>
        <img src="${catImage}" loading="lazy" class="food-image category-image" alt="${escapeHtml(catName)}" onerror="this.src='https://images.unsplash.com/photo-1504674900247-0877df9cc836?w=400&h=250&fit=crop&auto=format'">
    `;
    catItems.forEach(item => {
      finalHTML += `
        <div class="menu-item" data-name="${escapeHtml(item.name)}" data-price="${item.price}">
          <div class="item-info">
            <span class="item-name">${escapeHtml(item.name)}</span>
            <span class="price">₹${item.price}</span>
          </div>
          <button class="add-to-cart">+ Add</button>
        </div>
      `;
    });
    finalHTML += `</div></div>`;
  }
  
  menuContainer.innerHTML = finalHTML;
  
  // Attach add to cart event listeners
  document.querySelectorAll('.add-to-cart').forEach(btn => {
    btn.removeEventListener('click', addHandler);
    btn.addEventListener('click', addHandler);
  });
}

function addHandler(e) {
  const menuItem = e.currentTarget.closest('.menu-item');
  if (menuItem) {
    const name = menuItem.getAttribute('data-name');
    const price = parseInt(menuItem.getAttribute('data-price'), 10);
    if (name && !isNaN(price)) {
      addToCart(name, price);
    }
  }
}

function handleFilter() {
  renderMenu();
}

categorySelect.addEventListener('change', handleFilter);
searchInput.addEventListener('input', handleFilter);

// Order panel toggle
const orderPanel = document.getElementById('orderPanel');
const toggleBtn = document.getElementById('toggleOrderPanel');

function togglePanel() {
  if (orderPanel.style.display === 'none' || !orderPanel.style.display || orderPanel.style.opacity === '0') {
    orderPanel.style.display = 'block';
    setTimeout(() => {
      orderPanel.style.transform = 'scale(1)';
      orderPanel.style.opacity = '1';
      orderPanel.style.visibility = 'visible';
    }, 10);
  } else {
    orderPanel.style.transform = 'scale(0.95)';
    orderPanel.style.opacity = '0';
    orderPanel.style.visibility = 'hidden';
    setTimeout(() => {
      if (orderPanel.style.opacity === '0') orderPanel.style.display = 'none';
    }, 200);
  }
}

function showPanel() {
  orderPanel.style.display = 'block';
  setTimeout(() => {
    orderPanel.style.transform = 'scale(1)';
    orderPanel.style.opacity = '1';
    orderPanel.style.visibility = 'visible';
  }, 10);
}

toggleBtn.addEventListener('click', togglePanel);
openOrderPanelBtn.addEventListener('click', (e) => { e.preventDefault(); showPanel(); });

